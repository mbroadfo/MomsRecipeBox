// Main function
function main() {
  const args = process.argv.slice(2);
  
  if (args.length === 0) {
    showBanner();
    showUsage();
    return;
  }
  
  const command = args[0];
  
  switch (command) {
    case 'show':
      showProfile();
      break;
    case 'list':
      listProfiles();
      break;
    case 'set':
      if (args.length < 2) {
        log('‚ùå Profile name required', 'red');
        log('Usage: npm run profile:set <profile>', 'yellow');
        process.exit(1);
      }
      setProfile(args[1]);
      break;
    case 'start':
      startInfrastructure();
      break;
    case 'stop':
      stopInfrastructure();
      break;
    case 'validate':
      validateProfile();
      break;
    case 'dev':
      // Start development environment (infrastructure + UI if needed)
      startInfrastructure();
      // TODO: Add UI startup logic based on profile
      break;
    case 'help':
    case '--help':
      showUsage();
      break;
    default:
      log(`‚ùå Unknown command: ${command}`, 'red');
      showUsage();
      process.exit(1);
  }
}

// Run main function
main();

